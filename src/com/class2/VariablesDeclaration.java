package com.class2;

public class VariablesDeclaration {

	public static void main(String[] args) {
		// different ways to create/declare variables;
		// 1. create variable and assign value
		int num = 28;

		// 2 ways: using 2 steps
		int y; // 1. declaring variable

		y = 12; // 2. assign value

		// declare one by one
		int a;
		int b;
		int c;
		int d;
		int f;
		// or declare all together
		int x, m, z, w, v;
		a = 10;
		b = 20;

		byte j, k, l;
		j = 25;
		k = 21;
		l = 3;

		int aa = 10; int bb = 20; // not preferable but possible

	

	}

}
