package com.class7;

public class CompoumdOperators {
		//short hand assigned operators or compound operators
	public static void main(String[] args) {
	
		int a=10;
		a=a+10;
		System.out.println(a); //20
		
		//program always executes from top to bottom and from left to right
		
		int b=100;
		b=b+100;
		System.out.println(b); //200
		
		int c=100;
		c+=100;
		System.out.println(c); //200
		
		c+=35;
		System.out.println(c); //235
		
		c+=50;
		System.out.println(c); //285
		
		int d=30;
		d-=10;
		System.out.println(d); //20
		
		d+=50;
		System.out.println(d); //70
		
		d/=5;
		System.out.println(d); //14
		
		d%=2;
		System.out.println(d); //0
		
		// =+    addition              i+=5;     i=i+5;
		// -=    subtraction           j-=10;    j=j-10;
		// *=    multiplication        k*=2;     k=k*2;
		// /=    division              x/=10;    x=x/10;
		// %=    remainder             a%=4;     a=a%4;
		
		
		
		
		
		
		
	}

}
